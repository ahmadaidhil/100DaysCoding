public class day026 {
    public static void main(String[] args) {
        int angka = 10;

        angka += 2;
        System.out.println("angka: " + angka);
        angka -= 2;
        System.out.println("angka: " + angka);
        angka *= 5;
        System.out.println("angka: " + angka);
        angka /= 4;
        System.out.println("angka: " + angka);
        angka %= 4;
        System.out.println("angka: " + angka);
    }
}
